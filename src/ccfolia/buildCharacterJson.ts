export function buildCharacterJson(name: string, externalUrl: string, status: unknown[], params: unknown[], commands: string, initiative = 0, memo = "Generated by ytsheet2 converter", color?: string) {
  const data: Record<string, unknown> = { name, initiative, externalUrl, memo, status, params, commands };
  if (color) data.color = color;
  return { kind: "character", data };
}
